void loading_animation() {
    const char *animation = "|/-\\";
    for (int i = 0; i < 20; i++) {
        printf("\rLoading... %c", animation[i % 4]);
        fflush(stdout);
        usleep(200000);
    }
    printf("\rLoading... Done!\n");
}

